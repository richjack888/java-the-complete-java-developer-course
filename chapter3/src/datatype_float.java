public class datatype_float {
    public static void main(String[] args) {
        float MaxFloatValue = Float.MAX_VALUE;
        float MinFloatValue = Float.MIN_VALUE;
        System.out.println("MaxFloatValue : " + MaxFloatValue);
        System.out.println("MinFloatValue : " + MinFloatValue);
        float MyFloatValue = (float) 2.6;
        System.out.println("MyFloatValue : " + MyFloatValue);
        float MySecondFloatValue = 7.8f;
        System.out.println("MyFloatValue : " + MySecondFloatValue);
    }
}
