public class datatype_int {

    public static void main(String[] args) {

        int MaxIntValue = Integer.MAX_VALUE;
        int MinIntValue = Integer.MIN_VALUE;
        System.out.println("Max number:" + MaxIntValue);
        System.out.println("Min number:" + MinIntValue);
        System.out.println("Out Max number:" + (MaxIntValue + 1));
        System.out.println("Out Min number:" + (MinIntValue - 1));

    }

}
